let month = "march"

switch (month) {
    case "jan":
        console.log("january");
        
        break;
    case "feb":
        console.log("febuary");
        
        break;
    case "march":
        console.log("march");
        
        break;
    case "april":
        console.log("april");
        
        break;

    default:
        console.log("default case match");
        
        break;
}